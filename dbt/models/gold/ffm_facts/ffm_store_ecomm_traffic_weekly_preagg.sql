{# This aggregated model is used for FFM Campaign & Paid Media Dashboard
  - Dynamically enumerates all 2^4 (16) subsets of the dimensions:
      PRODUCT_FAMILY (PROD_FAM)
      PRODUCT_INDUSTRY (PROD_IND)
      ALLOCADIA_ID (ALL_ID)
      LAST_TOUCH_CHANNEL (LTC)
  - AGGREGATED_BY lists only the dimensions RETAINED.
        ALL (static at Country level) => WEEK-GDPR-GEO-CNTRY
        Full 4 dims => WEEK-GDPR-GEO-CNTRY-PROD_FAM-PROD_IND-ALL_ID-LTC
#}

