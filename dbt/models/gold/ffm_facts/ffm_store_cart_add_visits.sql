                    QUALIFY ROW_NUMBER() OVER (PARTITION BY ORDER_KEY ORDER BY LAST_UPDATED_DT DESC) = 1
                )

                SELECT
                t.ORDER_ID_EXTRACTED,
                f.CORPORATE_CSN,
                f.BUSINESS_TIER
                FROM TARGET_POS t
