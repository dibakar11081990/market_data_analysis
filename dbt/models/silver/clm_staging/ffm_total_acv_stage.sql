VICTIM_SURVIVOR AS (
    SELECT
        DISTINCT VICTIM_ACCOUNT_CSN, SURVIVING_ACCOUNT_CSN, FINAL_MERGE_STATUS
    FROM
        {{ source('EDM_PUBLIC', 'VICTIM_SURVIVOR_MAPPING') }}
