,SURVIVOR_MAPPING AS (
    SELECT
        FIN_SURV.*,
        PAR1.CORPORATE_PARENT_CSN_STATIC AS PARENT_SURVIVOR_CORPORATE_CSN,
        PAR_ACC_SURV.SURVIVING_ACCOUNT_CSN AS PARENT_SURVIVING_PARENTACCOUNT_CSN,
        COALESCE(
            PAR_ACC_SURV.SURVIVING_ACCOUNT_CSN, -- survivor of parent of survivor 
            PAR1.CORPORATE_PARENT_CSN_STATIC, -- parent of survivor csn 
            FIN_SURV.SURVIVOR_CORPORATE_CSN
        ) AS FINAL_CSN
    FROM(
        SELECT
            FIN.*,
            IFNULL(ACC_SURV.SURVIVING_ACCOUNT_CSN, FIN.CORPORATE_CSN) AS SURVIVOR_CORPORATE_CSN
        FROM
            FINMART_ACS_CORE FIN
            LEFT JOIN (
                SELECT DISTINCT VICTIM_ACCOUNT_CSN, SURVIVING_ACCOUNT_CSN
                FROM VICTIM_SURVIVOR
                WHERE FINAL_MERGE_STATUS = 'COMPLETED'
            ) ACC_SURV 
            ON FIN.CORPORATE_CSN = ACC_SURV.VICTIM_ACCOUNT_CSN
        ) FIN_SURV
        
        LEFT JOIN (
            SELECT
                T.ACCOUNT_CSN, A.CORPORATE_PARENT_CSN_STATIC
            FROM
            {{ source('ACCOUNT_OPTIMIZED', 'ACCOUNT_EDP_OPTIMIZED') }} A
            INNER JOIN {{ source('ACCOUNT_OPTIMIZED', 'TRANSACTIONAL_CSN_MAPPING_OPTIMIZED') }} T
            ON T.SITE_UUID_CSN = A.SITE_UUID_CSN
        ) PAR1 ON FIN_SURV.SURVIVOR_CORPORATE_CSN = PAR1.ACCOUNT_CSN

        LEFT JOIN (
            SELECT DISTINCT VICTIM_ACCOUNT_CSN, SURVIVING_ACCOUNT_CSN
            FROM VICTIM_SURVIVOR
        ) PAR_ACC_SURV ON PAR1.CORPORATE_PARENT_CSN_STATIC = PAR_ACC_SURV.VICTIM_ACCOUNT_CSN
)

