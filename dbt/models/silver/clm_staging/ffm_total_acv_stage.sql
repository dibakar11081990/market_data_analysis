    FROM {{ source('FINMART_PUBLISH_PRIVATE', 'VW_FINMART') }} FIN
    LEFT JOIN {{ ref('LOOKUP_FINANCE_CALENDAR') }} CAL
        ON FIN.TRANSACTION_DT = CAL.DATE_KEY

