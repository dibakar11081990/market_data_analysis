    LEFT JOIN(
        SELECT DISTINCT OWNEREXTERNALKEY, TO_VARCHAR (ID) AS ID FROM {{ source('ENTITLEMENT_PUBLIC', 'SUBSCRIPTION_PELICAN') }}
