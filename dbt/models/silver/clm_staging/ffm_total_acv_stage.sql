        AND TRY_TO_NUMBER(SPLIT_PART(FIN.FISCAL_WEEK, '_', 2)) = CAST(CAL.WEEK_NUMBER_FISCAL_QUARTER AS INT)
    WHERE
        DATA_SOURCE IN ('Unint. M&A', 'Innovyze Int.', 'Innovyze Unint.')
        AND HEDGE_INDICATOR = 'With VP'
        AND MYAB_INDICATOR = 'WITH MYAB'
