    LEFT JOIN(
        SELECT DISTINCT * FROM (
            SELECT F.*,G.* FROM (SELECT DISTINCT ORDERNUMBER__C,CONTACT__C FROM {{ source('SFDC_SHARED', 'ORDER__C') }}) F
            INNER JOIN 
            (SELECT DISTINCT OXYGEN_ID__C,ID FROM {{ var('CONTACT') }}) G
