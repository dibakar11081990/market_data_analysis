WITH WEEK_CALENDAR AS (
    SELECT DISTINCT
        FISCAL_YEAR_AND_FISCAL_QUARTER_NAME,
        WEEK_NUMBER_FISCAL_QUARTER,
        WEEK_START_DATE,
        FISCAL_QUARTERS_FROM_CURRENT_FISCAL_QUARTER
    FROM
        {{ ref('LOOKUP_FINANCE_CALENDAR') }}
    WHERE FISCAL_QUARTERS_FROM_CURRENT_FISCAL_QUARTER >= -12 AND DATE_KEY < CURRENT_DATE
),
-- Logic for extraction of CAMPAIGN_INDUSTRY from MPM ----
ALLOCADIA AS (
    SELECT
        DISTINCT LINE_ITEM_ID, CAMPAIGN_INDUSTRY, CHILD_CAMPAIGN_NAME, PARENT_CAMPAIGN_NAME, TACTIC_OWNER
    FROM
        {{ var('ALLOCADIA_LINEITEM_MASTER') }}
    WHERE
        ACTIVE_TO_DATE IS NULL
),
--Get all Order ID to Tactic Mapping with non null Campaign Industry
USTACK_DATA AS (
    SELECT
        TRIM(REGEXP_REPLACE(USTACK.CONV_PURCHASE_ID, '[^[:digit:]]', ' ')) AS CONV_PURCHASE_ID,
        NULLIF(USTACK.EVT_ALLOCADIA_ID, '-1') AS EVT_ALLOCADIA_ID,
        USTACK.EVT_CHANNEL,
        USTACK.CONV_REF_URL AS REF_URL,
        --ALID.CAMPAIGN_INDUSTRY,
        EVT_CAMPAIGN_ID || '_' || IFNULL(EVT_PLACEMENT_ID, '') || '_' || IFNULL(EVT_ADSET_ID, '') AS SPD_KEY,
        MAX(MIN_EVT_TIMESTAMP_UTC) EVT_TIMESTAMP
    FROM
        {{ var('FA_USER_STACK') }} USTACK
        --LEFT JOIN ALLOCADIA ALID ON CAST(EVT_ALLOCADIA_ID AS VARCHAR) = CAST(ALID.LINE_ITEM_ID AS VARCHAR)
    WHERE
        CONV_CONVERSION_TYPE = 'eStorePurchase'
        AND CONV_DATE BETWEEN $START_DATE AND $END_DATE
        AND CONV_PURCHASE_ID IS NOT NULL
        --AND CAMPAIGN_INDUSTRY IS NOT NULL
    GROUP BY
        ALL
),
--Get the latest touchpoint with non-null campaign industry
CAMPAIGN_INDUSTRY_LATEST AS (
    SELECT
        CONV_PURCHASE_ID,
        EVT_ALLOCADIA_ID,
        EVT_CHANNEL,
        REF_URL,
        --CAMPAIGN_INDUSTRY,
        SPD_KEY,
        ROW_NUMBER() OVER(
            PARTITION BY CONV_PURCHASE_ID
            ORDER BY
                EVT_TIMESTAMP DESC NULLS LAST
        ) RN
    FROM
        USTACK_DATA
    QUALIFY RN = 1
),
-- Remapping logic in MPM model MPM_USERFACT_METRICSCALC_STAGE
ALLOCADIA_ID_REMAPPING_FOR_INDUSTRY AS (
    SELECT
        A.CONV_PURCHASE_ID,
        ALID1.CAMPAIGN_INDUSTRY,
        ALID1.CHILD_CAMPAIGN_NAME,
        ALID1.PARENT_CAMPAIGN_NAME,
        ALID1.TACTIC_OWNER,
        COALESCE(TAXO.SPD_ALLOCADIA_ID, A.EVT_ALLOCADIA_ID) AS EVT_ALLOCADIA_ID,
        A.EVT_CHANNEL,
        A.REF_URL,
        ROW_NUMBER() OVER(
            PARTITION BY A.CONV_PURCHASE_ID
            ORDER BY ALID1.CAMPAIGN_INDUSTRY NULLS LAST
        ) AS RANK
    FROM
        CAMPAIGN_INDUSTRY_LATEST A
        LEFT JOIN (
            SELECT
                DISTINCT KEY,
                SPD_ALLOCADIA_ID
            FROM
                {{ ref('DIGITAL_EXECUTIONS_CAMPAIGN_TAXONOMY_MASTER') }}
            WHERE
                ACTIVE_TO IS NULL
        ) TAXO ON A.SPD_KEY = TAXO.KEY
        LEFT JOIN ALLOCADIA ALID1 ON COALESCE(CAST(TAXO.SPD_ALLOCADIA_ID AS VARCHAR),A.EVT_ALLOCADIA_ID) = CAST(ALID1.LINE_ITEM_ID AS VARCHAR)
    QUALIFY RANK = 1
),
